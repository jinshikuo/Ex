struct ngx_event_s {
		//事件相关的对象，通常data都是指向ngx_connection_t连接对象，开启文件异步I/O时，它可能会指向ngx_event_aio_t结构体
    void            *data;

		//标志位，为1时表示事件是可写的
    unsigned         write:1;
		//标志位，为1时表示为此事件可以建立新的连接，通常情况下，在ngx_cycle_t中的listening动态数组中，每一个监听对象ngx_listening_t对应的读事件中的accept标志位才会是1
    unsigned         accept:1;

    /* used to detect the stale events in kqueue, rtsig, and epoll */
		//这个标志位用于区分当前事件是否是过期的，它仅仅是给事件驱动模块使用的，。当看是处理一批事件时，处理前面的事件可能会关闭一些连接，而这些连接有可能影响这批事件中还未处理到的后面的事件
		//可通过instance标志位来避免处理后面的过期事件
    unsigned         instance:1;

    /*
     * the event was passed or would be passed to a kernel;
     * in aio mode - operation was posted.
     */
    unsigned         active:1;

    unsigned         disabled:1;

    /* the ready event; in aio mode 0 means that no operation can be posted */
    unsigned         ready:1;

    unsigned         oneshot:1;

    /* aio operation is complete */
    unsigned         complete:1;

    unsigned         eof:1;
    unsigned         error:1;

    unsigned         timedout:1;
    unsigned         timer_set:1;
		//标志位，delayed为1时表示需要延迟处理这个事件，它仅限于限速功能
    unsigned         delayed:1;

    unsigned         read_discarded:1;

    unsigned         unexpected_eof:1;
		//标志位，为1表示延迟建立TCP连接，也就是说，TCP三次握手后并不建立连接，而是要等到真正收到数据包后才建立TCP连接
    unsigned         deferred_accept:1;

    /* the pending eof reported by kqueue or in aio chain operation */
    unsigned         pending_eof:1;

    unsigned         available:1;

    ngx_event_handler_pt  handler;

    ngx_uint_t       index;

    ngx_log_t       *log;

    ngx_rbtree_node_t   timer;

    unsigned         closed:1;

    /* to test on worker exit */
    unsigned         channel:1;
    unsigned         resolver:1;


    /* the links of the posted queue */
    ngx_event_t     *next;
    ngx_event_t    **prev;
};



typedef struct {
    ngx_int_t  (*add)(ngx_event_t *ev, ngx_int_t event, ngx_uint_t flags);
    ngx_int_t  (*del)(ngx_event_t *ev, ngx_int_t event, ngx_uint_t flags);

    ngx_int_t  (*enable)(ngx_event_t *ev, ngx_int_t event, ngx_uint_t flags);
    ngx_int_t  (*disable)(ngx_event_t *ev, ngx_int_t event, ngx_uint_t flags);

    ngx_int_t  (*add_conn)(ngx_connection_t *c);
    ngx_int_t  (*del_conn)(ngx_connection_t *c, ngx_uint_t flags);

    ngx_int_t  (*process_changes)(ngx_cycle_t *cycle, ngx_uint_t nowait);
    ngx_int_t  (*process_events)(ngx_cycle_t *cycle, ngx_msec_t timer,
                   ngx_uint_t flags);

    ngx_int_t  (*init)(ngx_cycle_t *cycle, ngx_msec_t timer);
    void       (*done)(ngx_cycle_t *cycle);
} ngx_event_actions_t;


